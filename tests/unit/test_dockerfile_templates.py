from __future__ import annotations

import unittest
from dataclasses import fields

from tests.test_support import add_src_to_path

add_src_to_path()

from springdocker.dockerfile import DockerfileOptions, build_dockerfile


class DockerfileTemplateRenderingTests(unittest.TestCase):
    def test_template_render_keeps_generated_header(self) -> None:
        rendered = build_dockerfile(DockerfileOptions(build_tool="maven", java_version=21))
        self.assertIn("# Generated by springdocker", rendered)
        self.assertIn("# Java 21 | build-tool: maven", rendered)

    def test_to_spec_maps_all_option_fields(self) -> None:
        option_field_names = {field.name for field in fields(DockerfileOptions)}
        self.assertEqual(
            option_field_names,
            {
                "build_tool",
                "recipe",
                "java_version",
                "use_buildkit_cache",
                "use_jlink",
                "non_root",
                "tuned_jvm_flags",
                "must_have_modules",
                "runtime_image",
                "platform_aware",
                "healthcheck_path",
                "include_oci_labels",
                "include_stopsignal",
                "include_embedded_sbom",
                "include_reproducible_controls",
                "use_layered_jar",
                "enable_appcds",
            },
        )

    def test_toggle_markers_render_as_expected(self) -> None:
        rendered = build_dockerfile(
            DockerfileOptions(
                build_tool="maven",
                include_oci_labels=False,
                include_stopsignal=False,
                include_embedded_sbom=False,
                include_reproducible_controls=False,
                use_layered_jar=False,
                enable_appcds=False,
                healthcheck_path="/actuator/health/readiness",
            )
        )
        self.assertNotIn("org.opencontainers.image.source", rendered)
        self.assertNotIn("STOPSIGNAL SIGTERM", rendered)
        self.assertNotIn("/usr/share/sbom/spdx.json", rendered)
        self.assertNotIn("SOURCE_DATE_EPOCH", rendered)
        self.assertNotIn("jarmode=layertools", rendered)
        self.assertIn('ENTRYPOINT ["java", "-XX:MaxRAMPercentage=75", "-XX:+ExitOnOutOfMemoryError", "-Djava.io.tmpdir=/tmp", "-jar", "app.jar"]', rendered)
        self.assertIn("HEALTHCHECK --interval=15s", rendered)

    def test_to_spec_groups_values(self) -> None:
        options = DockerfileOptions(
            build_tool="gradle",
            recipe="spring-aot",
            java_version=21,
            use_buildkit_cache=False,
            use_jlink=False,
            non_root=False,
            tuned_jvm_flags=False,
            must_have_modules=("java.naming",),
            runtime_image="distroless",
            platform_aware=False,
            healthcheck_path="/health",
            include_oci_labels=False,
            include_stopsignal=False,
            include_embedded_sbom=False,
            include_reproducible_controls=False,
            use_layered_jar=False,
            enable_appcds=False,
        )
        spec = options.to_spec()
        self.assertEqual(spec.build_tool, "gradle")
        self.assertEqual(spec.java_version, 21)
        self.assertEqual(spec.build.recipe, "spring-aot")
        self.assertFalse(spec.build.use_buildkit_cache)
        self.assertFalse(spec.build.use_jlink)
        self.assertEqual(spec.build.must_have_modules, ("java.naming",))
        self.assertEqual(spec.runtime.runtime_image, "distroless")
        self.assertFalse(spec.runtime.platform_aware)
        self.assertFalse(spec.runtime.non_root)
        self.assertEqual(spec.runtime.healthcheck_path, "/health")
        self.assertFalse(spec.supply_chain.include_oci_labels)
        self.assertFalse(spec.supply_chain.include_stopsignal)
        self.assertFalse(spec.supply_chain.include_embedded_sbom)
        self.assertFalse(spec.supply_chain.include_reproducible_controls)


if __name__ == "__main__":
    unittest.main()
